'''a python script to demonstrate String 
Concatenation adding space in between 
(Given Strings a=”Learning” b=”Python” ) '''
    
a = "Learning"
b = "Python"
# String concatenation

print(f"\n1. {a} {b}")

print("\n2. %s %s"%(a, b))

print("\n3. {} {}". format(a,b))

print("\n4.",' '.join([a,b]))







